/* A01 ===============================================================*/

function playclipA01() {
  if (navigator.appName == "Microsoft Internet Explorer") {
    if (document.all) {
      document.all.soundA01.src = "audio/A01.wav";
    }
  }

  else {
    var audio = document.getElementsByClassName("A01")[0];
    audio.play();
  }
}

